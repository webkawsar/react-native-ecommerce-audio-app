import { createSlice } from '@reduxjs/toolkit';


const todoSlice = createSlice({
    name: 'todos',
    initialState: {
        value: ['Todo 1', 'Todo 2']
    },
    reducers: {
        getTodo(state) {
            state.value[0];
        }
    }
})


export const {getTodo} = todoSlice.actions;

export default todoSlice.reducer;


